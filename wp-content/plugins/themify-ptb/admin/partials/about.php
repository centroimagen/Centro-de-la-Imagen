	<h2><?php _e('About Post Type Builder', 'ptb') ?></h2>
	<p><a href="//themify.me/post-type-builder" target="blank">Post Type Builder</a> (PTB) is an &quot;all-in-one&quot; plugin that allows you to create custom post types, taxonomies, and post type templates.</p>

	<h3>Step-by-Step Overview</h3>
	<p>Below is a quick step-by-step overview on how to use the plugin. You may click on the links to read the detail documentation of each step.</p>
	<ol>
		<li>Create a new <a href="//themify.me/docs/post-type-builder-plugin-documentation#post-types" target="blank">Post Type</a>
			<ul>
				<li>- Configure the <a href="//themify.me/docs/post-type-builder-plugin-documentation#meta-box-builder" target="blank">Meta Box Builder</a> (custom fields)</li>
				<li>- Create optional <a href="//themify.me/docs/post-type-builder-plugin-documentation#taxonomies" target="blank">Taxonomies</a> associated with the Post Type (taxonomy is like a type/group of the post type)</li>
			</ul>
		</li>
		<li>Create <a href="//themify.me/docs/post-type-builder-plugin-documentation#template-builder" target="blank">Templates</a> for the post type
			<ul>
				<li>- Edit the Archive Template</li>
				<li>- Edit the Single Template</li>
			</ul>
		</li>
		<li>When you are done with the post type and templates, you'll be ready to add custom posts</li>
		<li>After custom posts are added, you may use the <a href="//themify.me/docs/post-type-builder-plugin-documentation#ptb-shortcode" target="blank">shortcode generator</a> (located on the WordPress Classic Editor) to display custom posts on any post/page.</li>
	</ol>

